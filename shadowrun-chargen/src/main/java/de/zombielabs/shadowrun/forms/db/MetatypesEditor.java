    private void loadMetatypes(boolean reload) throws SQLException {
        if(reload) { this.data.reloadMetatypes(); }
        DefaultListModel model = new DefaultListModel();
        for(final Metatype meta : this.data.getMetatypes()) {
            model.addElement(meta);
        }
        this.listMetatypes.setModel(model);
    }
    

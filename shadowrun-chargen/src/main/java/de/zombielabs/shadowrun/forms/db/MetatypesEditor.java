    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         if(this.edited == null || this.newMeta) { return; }
        
        String q = "DELETE FROM metatypes WHERE id = " + this.edited.getId();
        try {
            int ans = JOptionPane.showConfirmDialog(this, "Löschen: " + this.edited + " ?", "Sure?", JOptionPane.YES_NO_OPTION);
            if(ans == JOptionPane.YES_OPTION) {
                this.data.update(q);
                this.edited = null;
                this.newMeta = false;
                this.resetForm(null);

                this.loadMetatypes(true);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(PerkEditor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(this.edited == null) { return; }
        String q = null;

        if(!newFlaw) {
            q = "UPDATE flaws SET name = '" + this.txtName.getText() + "', cost = " + this.txtCost.getValue() + ", description = '" + this.txtDescription.getText() + "' WHERE id = " + this.edited.getId();
        } else {
            q = "INSERT INTO flaws (name, cost, description) VALUES ('" + this.txtName.getText() + "', " + this.txtCost.getValue() + ", '" + this.txtDescription.getText() + "');";
        }

        try {
            this.data.update(q);
            this.edited = null;
            this.newFlaw = false;
            this.txtName.setText(null);
            this.txtCost.setValue(0);
            this.txtDescription.setText(null);
            this.listFlaws.clearSelection();

            this.loadFlaws(true);

        } catch (SQLException ex) {
            Logger.getLogger(PerkEditor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(this.edited == null || this.newFlaw) { return; }

        String q = "DELETE FROM flaws WHERE id = " + this.edited.getId();
        try {
            int ans = JOptionPane.showConfirmDialog(this, "LÃ¶schen: " + this.edited + " ?", "Sure?", JOptionPane.YES_NO_OPTION);
            if(ans == JOptionPane.YES_OPTION) {
                this.data.update(q);
                this.edited = null;
                this.newFlaw = false;
                this.txtName.setText(null);
                this.txtCost.setValue(0);
                this.txtDescription.setText(null);
                this.listFlaws.clearSelection();

                this.loadFlaws(true);
            }

        } catch (SQLException ex) {
            Logger.getLogger(PerkEditor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed


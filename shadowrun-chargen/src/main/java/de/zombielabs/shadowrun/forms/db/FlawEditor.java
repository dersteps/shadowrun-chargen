    private void loadFlaws(boolean reload) throws SQLException {
        if(reload) { this.data.reloadFlaws(); }
        DefaultListModel model = new DefaultListModel();
        for(final Flaw flaw : this.data.getFlaws()) {
            model.addElement(flaw);
        }
        
        this.listFlaws.setModel(model);
    }

